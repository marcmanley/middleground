#!/usr/bin/env node
/*
 * Generates the blog card grid in blog/index.html from the individual blog
 * post pages. Each post's own page is the single source of truth for its
 * card -- this script never invents content, it only reads and reuses what
 * already exists on the post (title, canonical URL, <time datetime>, author
 * byline, category badge(s)), plus a few small fields that genuinely cannot
 * be derived from any existing standard and so live in their own <meta> tags:
 *
 *   <meta name="card-excerpt" content="...">   -- short archive-card blurb
 *                                                  (deliberately shorter/
 *                                                  punchier than the SEO
 *                                                  meta description)
 *   <meta name="card-featured" content="true">  -- opts a post into the
 *                                                  highlighted "clay" card
 *                                                  style; omitted = normal
 *   <meta name="card-title" content="...">      -- only on posts whose own
 *                                                  <title> isn't English
 *                                                  (the archive is English-
 *                                                  context); omitted = use
 *                                                  <title> as-is
 *
 * blog/index.html itself supplies the header, footer, and everything else
 * around the grid -- this script only replaces the HTML between the
 * BLOG_CARDS:START / BLOG_CARDS:END markers inside it. Nothing here is a
 * template; it edits the real page in place.
 *
 * WHICH FILES ARE SCANNED
 *   Every *.html file in blog/, except:
 *     - index.html itself
 *     - dotfiles / hidden files
 *     - any page with <meta name="robots" content="...noindex...">
 *       (the same draft/exclusion convention generate-sitemap.js already
 *       uses -- reused here rather than inventing a second one)
 *
 * SORT ORDER
 *   Strictly newest-first by each post's own <time datetime="..."> value.
 *   Never by filesystem time, git history, filename, or discovery order.
 *   Ties (posts sharing an identical datetime) are broken by filename so
 *   the output is fully deterministic build to build.
 *
 * VALIDATION
 *   A post missing any required field (title, canonical URL, <time
 *   datetime>, category badge, and an excerpt from either card-excerpt or
 *   the SEO meta description) fails the build with a clear error naming the
 *   file and the missing field, rather than silently producing a bad card.
 *
 * USAGE
 *   node generate-blog-index.mjs
 *   Run automatically as the first step of `npm run build`.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.dirname(fileURLToPath(import.meta.url));
const BLOG_DIR = path.join(ROOT, 'blog');
const INDEX_FILE = path.join(BLOG_DIR, 'index.html');

const START_MARKER = '<!-- BLOG_CARDS:START — generated by generate-blog-index.mjs. Do not hand-edit; edit the source post\'s metadata instead, then run `npm run build`. -->';
const END_MARKER = '<!-- BLOG_CARDS:END -->';

const DEFAULT_AVATAR = {
  src: '../media/images/web/hero-imam.jpg',
  width: '1400',
  height: '787',
};

function listCandidatePostFiles() {
  return fs
    .readdirSync(BLOG_DIR, { withFileTypes: true })
    .filter((e) => e.isFile() && e.name.endsWith('.html'))
    .map((e) => e.name)
    .filter((name) => name !== 'index.html' && !name.startsWith('.'))
    .sort(); // deterministic base order before the date sort below
}

function isNoindex(html) {
  const match = html.match(
    /<meta\s+[^>]*name=["']robots["'][^>]*content=["']([^"']*)["'][^>]*>/i
  );
  return !!match && /noindex/i.test(match[1]);
}

function extractAttr(html, tagRe) {
  const m = html.match(tagRe);
  return m ? m[1] : null;
}

function formatDisplayDate(isoDatetime) {
  // Parse the date part only, as local calendar values, so the displayed
  // date never shifts a day due to UTC/local timezone conversion.
  const datePart = isoDatetime.split('T')[0];
  const [year, month, day] = datePart.split('-').map(Number);
  const date = new Date(year, month - 1, day);
  return date.toLocaleDateString('en-US', {
    month: 'short',
    day: 'numeric',
    year: 'numeric',
  });
}

function extractCategoryBadges(html, file) {
  // The category eyebrow badge(s) live between the "Back to Blog" link and
  // the <h1> on every post page -- this reads that block rather than
  // introducing a separate category field.
  const backToBlogIdx = html.search(/Back to Blog/);
  const h1Idx = html.indexOf('<h1', backToBlogIdx);
  if (backToBlogIdx === -1 || h1Idx === -1) {
    throw new Error(`${file}: could not locate the category badge block above <h1>`);
  }
  const region = html.slice(backToBlogIdx, h1Idx);
  const spanRe = /<span class="([^"]+)">\s*([\s\S]*?)\s*<\/span>/g;
  const badges = [];
  let m;
  while ((m = spanRe.exec(region)) !== null) {
    badges.push({ className: m[1], text: m[2].trim() });
  }
  if (badges.length === 0) {
    throw new Error(`${file}: no category badge found above <h1>`);
  }
  return badges;
}

function parsePost(file) {
  const html = fs.readFileSync(path.join(BLOG_DIR, file), 'utf8');

  if (isNoindex(html)) return null;

  // card-title exists only for the handful of posts whose <title> is not in
  // English (the archive page itself is English-context) -- everywhere else
  // the card reuses the page's own <title> verbatim.
  const titleMeta = extractAttr(html, /<meta name="card-title" content="([^"]*)">/);
  const rawTitle = extractAttr(html, /<title>([^<]+)<\/title>/);
  if (!titleMeta && !rawTitle) throw new Error(`${file}: missing <title>`);
  // Keep the title's own HTML entities as-is (e.g. "&amp;") -- this text is
  // re-embedded directly into blog/index.html, which is HTML too.
  const title = titleMeta ?? rawTitle.replace(/\s*—\s*Middle Ground\s*$/, '');

  const canonical = extractAttr(html, /<link\s+rel="canonical"\s+href="([^"]+)">/);
  if (!canonical) throw new Error(`${file}: missing <link rel="canonical">`);

  const timeMatch = html.match(/<time datetime="([^"]+)">/);
  if (!timeMatch) throw new Error(`${file}: missing <time datetime="...">`);
  const datetime = timeMatch[1];

  const excerptMeta = extractAttr(html, /<meta name="card-excerpt" content="([^"]*)">/);
  const descriptionMeta = extractAttr(html, /<meta name="description" content="([^"]*)">/);
  const excerpt = excerptMeta ?? descriptionMeta;
  if (!excerpt) throw new Error(`${file}: no card-excerpt or meta description to use as an excerpt`);

  const featured = /<meta name="card-featured" content="true">/.test(html);

  const badges = extractCategoryBadges(html, file);
  const category = badges[0].text;
  // A secondary badge (e.g. "In Arabic · بالعربية") is reused on the card in
  // its short form -- the card is a small space, so we drop the native-
  // script suffix the post page itself displays after " · ".
  const secondaryBadge = badges[1] ? badges[1].text.split('&middot;')[0].trim() : null;

  return { file, title, canonical, datetime, excerpt, featured, category, secondaryBadge };
}

function collectPosts() {
  const posts = [];
  for (const file of listCandidatePostFiles()) {
    const post = parsePost(file);
    if (post) posts.push(post);
  }
  posts.sort((a, b) => new Date(b.datetime) - new Date(a.datetime));
  return posts;
}

function renderCard(post, index) {
  const { file, title, excerpt, featured, category, secondaryBadge, datetime } = post;
  const displayDate = formatDisplayDate(datetime);
  const isStone = index % 2 === 1;

  let cardClass, badgeClass, secondaryBadgeClass, h2Class, pClass, metaClass;
  if (featured) {
    cardClass = 'group flex flex-col bg-clay text-cream rounded-xl p-7 shadow-soft hover:shadow-soft-lg transition-shadow btn-focus';
    badgeClass = 'inline-block self-start text-xs tracking-widest uppercase text-clay bg-cream rounded-full px-3 py-1';
    secondaryBadgeClass = badgeClass;
    h2Class = 'text-2xl leading-snug mb-3';
    pClass = 'text-cream/85 text-sm leading-relaxed mb-6 flex-1';
    metaClass = 'flex items-center gap-2 text-xs text-cream/80';
  } else {
    cardClass = isStone
      ? 'group flex flex-col bg-stone/30 rounded-xl p-7 shadow-soft hover:shadow-soft-lg transition-shadow btn-focus'
      : 'group flex flex-col bg-cream border border-ink/10 rounded-xl p-7 shadow-soft hover:shadow-soft-lg transition-shadow btn-focus';
    badgeClass = `inline-block self-start text-xs tracking-widest uppercase text-earth ${isStone ? 'bg-cream' : 'bg-stone/40'} rounded-full px-3 py-1`;
    secondaryBadgeClass = 'inline-block self-start text-xs tracking-widest uppercase text-cream bg-sky rounded-full px-3 py-1';
    h2Class = 'text-2xl leading-snug mb-3 group-hover:text-clay transition-colors';
    pClass = 'text-ink/65 text-sm leading-relaxed mb-6 flex-1';
    metaClass = 'flex items-center gap-2 text-xs text-ink/50';
  }

  const badgesHtml = secondaryBadge
    ? `      <div class="flex flex-wrap gap-2 mb-5">
        <span class="${badgeClass}">${category}</span>
        <span class="${secondaryBadgeClass}">${secondaryBadge}</span>
      </div>\n`
    : `      <span class="${badgeClass} mb-5">${category}</span>\n`;

  return `    <a href="${file}" class="${cardClass}">
${badgesHtml}      <h2 class="${h2Class}">${title}</h2>
      <p class="${pClass}">${excerpt}</p>
      <div class="${metaClass}">
        <img src="${DEFAULT_AVATAR.src}" alt="" width="${DEFAULT_AVATAR.width}" height="${DEFAULT_AVATAR.height}" class="h-6 w-6 rounded-full object-cover object-center">
        <span>Imam Marc Manley</span>
        <span>&middot;</span>
        <time datetime="${datetime}">${displayDate}</time>
      </div>
    </a>`;
}

function main() {
  const posts = collectPosts();
  const cardsHtml = posts.map(renderCard).join('\n\n');

  const indexHtml = fs.readFileSync(INDEX_FILE, 'utf8');
  const startIdx = indexHtml.indexOf(START_MARKER);
  const endIdx = indexHtml.indexOf(END_MARKER);
  if (startIdx === -1 || endIdx === -1) {
    throw new Error('blog/index.html is missing BLOG_CARDS:START / BLOG_CARDS:END markers');
  }

  const before = indexHtml.slice(0, startIdx + START_MARKER.length);
  const after = indexHtml.slice(endIdx);
  const nextHtml = `${before}\n\n${cardsHtml}\n\n    ${after}`;

  if (nextHtml === indexHtml) {
    console.log(`blog/index.html: already up to date (${posts.length} post(s)).`);
    return;
  }

  fs.writeFileSync(INDEX_FILE, nextHtml);
  console.log(`blog/index.html: regenerated with ${posts.length} post(s).`);
}

main();
